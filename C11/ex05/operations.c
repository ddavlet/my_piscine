#include "header.h"

int	ft_add(int arg1, int arg2)
{
	return (arg1 + arg2);
}
int	ft_subtract(int arg1, int arg2)
{
	return (arg1 - arg2);
}
int	ft_divide(int arg1, int arg2)
{
	return (arg1 / arg2);
}
int	ft_multiply(int arg1, int arg2)
{
	return (arg1 * arg2);
}
int	ft_modulo(int arg1, int arg2)
{
	return (arg1 % arg2);
}
